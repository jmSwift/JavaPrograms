
public class charAt
{
	public static void main(String[] args)
	{
		String myName = "joseMEza";
		
		System.out.println("myName.charAt(3) : " + myName.charAt(3));
		System.out.println("myName.substring(4) is : " + myName.substring(4));
		System.out.println("myName.length is " + myName.length());
	}
}